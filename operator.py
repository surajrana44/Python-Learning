# Arithmatic Operator

x=10
y=20

print(x+y) #add
print(x-y) #subtract
print(x/y)  #divison
print(x*y)  #multiply
print(x%y)  #modulas- remainder
print(x**y) #exponent
print(x//y) #floor divison

# Comparison Operator 

x=10
y=5

print(x==y)
print(x!=y)
print(x>y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)

# Logical Operator 

age=26
is_student=True

print(age>18 and is_student)
print(age<25 or is_student)
print(not is_student)

# Assignment operator

x=10
x+=5 # +=,-=,*=
print(x)

# Identity Operator (is,is not)

a=[1,2,3]
b=a
c=[1,2,3]

print(a is b) #compare object memory
print(a is c)

# Membership Operator (in,not in)

vegetable=['aloo','tamatar','karela']

print('bhindi' in vegetable) # data checking in squence
print('aloo' not in vegetable)

